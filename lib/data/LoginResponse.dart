class Autogenerated {
  bool? success;
  LoginResponse? data;
  String? message;

  Autogenerated({this.success, this.data, this.message});

  Autogenerated.fromJson(Map<String, dynamic> json) {
    success = json['success'];
    data = json['data'] != null ? new LoginResponse.fromJson(json['data']) : null;
    message = json['message'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = new Map<String, dynamic>();
    data['success'] = this.success;
    if (this.data != null) {
      data['data'] = this.data!.toJson();
    }
    data['message'] = this.message;
    return data;
  }
}

class LoginResponse {
  String? username;
  String? accessToken;
  String? tokenType;
  String? error;

  LoginResponse({this.username, this.accessToken, this.tokenType});

  LoginResponse.fromJson(Map<String, dynamic> json) {
    username = json['username'];
    accessToken = json['access_token'];
    tokenType = json['token_type'];
  }

  LoginResponse.withError(String errorMessage) {
    error = errorMessage;
  }
  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = new Map<String, dynamic>();
    data['username'] = this.username;
    data['access_token'] = this.accessToken;
    data['token_type'] = this.tokenType;
    return data;
  }
}
